let rotator = (len, rot, arr) => {
  let result = [];
  for (let i = 0; i < len; i++) {
    if ((i - rot) < 0) {
      result[len + (i - rot)] = arr[i];
    } else {
      result[i - rot] = arr[i];
    }
  }
  console.log(result.join(' '));
}

arr = [1, 2, 3, 4, 5];
rotator(5, 4, arr)

arr = [1, 2, 3, 4, 5, 6];
rotator(6, 3, arr)


